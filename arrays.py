

# ARRAY MODULE EXAMPLES
print("ARRAYS")

# 1. Creating an array
arr =  [1, 2, 3, 4, 5]
print(arr)

# 2. Append an element
arr.append(6)
print(arr)

# 3. Extend the array
arr.extend([7, 8])
print( arr)

# 4. Insert at index
arr.insert(2, 99)
print(arr)

# 5. Remove an element
arr.remove(99)
print(arr)

# 6. Pop an element
popped = arr.pop(2)
print(popped)

# 7. Index of an element
index = arr.index(2)
print(index)

# 8. Count occurrences
count = arr.count(5)
print(count)

# 9. Reverse the array
arr.reverse()
print(arr)



